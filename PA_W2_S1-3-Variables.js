/*
	Number Variables Conceptual
	The + sign can do concatenation with strings or do
	addition operator with numbers
*/

//Concatenation
let stringVar = "I like" + " cats";

//Addition Operator
let numberVar = 9 + 3;

//Ways to change number variables
let plants = 12;
//Reassign, but no computing
plants = 15;

//Add 3 new plants
plants = plants + 3;
//Remove 2 plants
plants = plants - 2;
//short hand add 4 using +=
plants += 4;
//shorthand minus 3 using -=
plants -=3;
//increment plants by 1 using ++
plants ++;
//decrement plants by 1 using --
plants --;